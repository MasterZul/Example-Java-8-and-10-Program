package com.MasterZul;

public class Dimensions {

    private int widht;
    private int width;
    private int depth;

    public Dimensions(int widht, int width, int depth) {
        this.widht = widht;
        this.width = width;
        this.depth = depth;
    }

    public int getWidht() {
        return widht;
    }

    public int getWidth() {
        return width;
    }

    public int getDepth() {
        return depth;
    }
}
